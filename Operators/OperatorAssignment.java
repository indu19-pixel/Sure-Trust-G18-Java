public class OperatorAssignment {
    public static void main(String args[]){
        int a=10,b=5;

        //Addition Assign
        a+=b;
        System.out.println(a);

        //Subtraction Assign
        a-=b;
        System.out.println(a);

        //Multiplication Assign
        a*=b;
        System.out.println(a);

        //Modulus Assign
        a%=b;
        System.out.println(a);

        //Division Assign
        a/=b;
        System.out.println(a);    }
}
