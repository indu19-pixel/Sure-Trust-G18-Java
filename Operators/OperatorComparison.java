public class OperatorComparison {
    public static void main(String args[]){
        int a=10,b=5;

        //Not equal "!"
        if(a!=b){
            System.out.println("a not equal to b");
            //Greater than ">"
            if (a>b){
                System.out.println("a greater than b");
            }
            //Less than "<"
            else if (a<b){
                System.out.println("a less than b");
            }
        }
        //Equal to "=="
        else{
            System.out.println("a equal to b");
        }


    }
}
