//Program on N Natural numbers

import java.util.*;

public class N_NaturalNumbers  {

    public static void main(String args[]){

       Scanner sc=new Scanner(System.in);
       System.out.println("Enter value n: ");
       int n=sc.nextInt();
       int i=1;
       while(i<=n){
        System.out.println(i);
        i++;
       }
    
    }
}
